#include "enemy.h"

#include <string>
#include <iostream>
using namespace std;

enemy::enemy(string description , int damage) {
    this->description;
    this->damage;
}

enemy::~enemy(){
}

template<typename T>
void enemy::setDamage(const T& damge){
    if(damge > 100)
        cout << "This exceeds the maximum damage , must be between 0 and 100)";
    else
        damge = damage;
}

string enemy::getDescription(){
    return this->description;
}

int enemy::getDamage(){
    return this ->damage;
}
